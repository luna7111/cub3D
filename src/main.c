#include <cub3d.h>

int	main(int argn, char **argv)
{
	t_gctrl	*gctrl;
	t_game	*game;

	if (argn != 2)
	{
		printf("Error: Wrong number of arguments.\n");
		return (0);
	}
	gctrl = gctrl_init();
	game = init_game(gctrl, argv[1]);
	init_hooks(game);
	mlx_loop(game->mlx);
	safe_exit(game);
}
